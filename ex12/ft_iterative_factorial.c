int	ft_iterative_factorial(int nb)
{
	int	x;

	x = nb;
	if (nb > 12 || nb < 0)
		return (0);
	if (nb == 0)
		return (1);
	while (nb >= 2)
	{
		x = x * (nb - 1);
		nb--;
	}
	return (x);
}
